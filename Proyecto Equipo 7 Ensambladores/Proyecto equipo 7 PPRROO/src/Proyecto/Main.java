    private void smnCargarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_smnCargarArchivoActionPerformed
        JFileChooser selecc = new JFileChooser();
        FileNameExtensionFilter filterArch = new FileNameExtensionFilter("ASM", "asm");
        selecc.setFileFilter(filterArch);
        int option = selecc.showOpenDialog(this);
        if (option == JFileChooser.APPROVE_OPTION) {
            File fichero = selecc.getSelectedFile();
            if (fichero.getName().endsWith(".asm")) {
                nombreArchivo = selecc.getSelectedFile().getName();
                rutaArchivo = selecc.getSelectedFile().toString();
                try (FileReader fr = new FileReader(fichero)) {
                    archivo = "";
                    int valor = fr.read();
                    while (valor != -1) {
                        archivo = archivo + (char) valor;
                        valor = fr.read();
                    }
                    //txtArea.setText(archivo);
                    construirArchivo();
                } catch (IOException e1) {
                    JOptionPane.showMessageDialog(null,"Error al abrir el archivo 1");
                }
            }else {
                JOptionPane.showMessageDialog(null,"Error el archivo es inválido ");
            }
        }
    }//GEN-LAST:event_smnCargarArchivoActionPerformed

