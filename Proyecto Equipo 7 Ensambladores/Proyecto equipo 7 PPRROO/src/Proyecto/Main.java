    public void construirArchivo() throws FileNotFoundException{
        int cont=1;
        paginasA=1;
        tamA=archivo.length();
        for (int i = 0; i < tamA; i++) {
            if (archivo.charAt(i) == '\n') {
                if (cont == imprimir) {
                    paginasA++;
                    cont=1;
                }else{
                    cont++;
                }
            }
        }
        paginaA=1;
        mostrarArchivo();
        //lblPaginaA.setText("Pagina: "+paginaA+" de "+paginasA);
        pnlseparar.archivo=archivo;
        pnlsimbolos = new pnlSimbolos(nombreArchivo,rutaArchivo,this);
        seAbrioCorrecto=true;
    }
    public void mostrarArchivo(){
        int cont=1;
        int renglonObjetivo=paginaA;
        int renglonBuscador=1;
        String pag="";
        int i=0;
        while(renglonBuscador<renglonObjetivo){
            if (archivo.charAt(i) == '\n') {
                if(cont==imprimir){
                    renglonBuscador++;
                    cont=1;
                }else{
                    cont++;
                }
            }
            i++;
        }
        //pag +=list+": ";
        while(renglonBuscador<=renglonObjetivo&&i<tamA){
            pag += archivo.charAt(i);
            if (archivo.charAt(i) == '\n') {
                if (cont==imprimir) {
                    renglonBuscador++;
                    cont=1;
                }else{
                    cont++;
                }
            }
            i++;
        }
        txtArchivo.setText(pag);
    }
